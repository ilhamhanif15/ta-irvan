        <br>
        Kegiatan outbond ini sendiri melibatkan seluruh anggota OSIS (kakak tingkat) dan juga pendidik dan tenaga kependidikan SMA Negeri 1 Tambang. Semua keluarga besar SMA Negeri Plus Provinsi Riau membaur menjadi satu  dan mengikuti kegiatan outbond ini sampai selesai. Outbound ini juga sekaligus sebagai refreshing seluruh anggota keluarga besar SMA Negeri 1 Tambang.
        <br>
        <br>
