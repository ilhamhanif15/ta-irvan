        August 7, 2019 <br/><br/>
        Sebanyak 150 siswa kelas X mengikuti kegiatan outbond di taman agrowisata Sungai Pinang Nadin Kabupaten Kampar (Minggu/4 Agustus 2019). Menurut Waka. Kesiswaan SMAN 1Tambang, Drs.H.Hasidin Pospos, M.Pd “ Kegiatan outbond ini merupakan salah satu proses yang harus diikuti oleh peserta didik baru SMAN Plus Provinsi Riau. Kegiatan outbond ini sangat bermanfaat bagi mereka yang selama 3 tahun kedepan akan tinggal di asrama. Pembentukan karakter siswa, team building dan team work akan sangat bermanfaat bagi anak anak karena mereka selama 3 tahun kedepan akan dituntut untuk hidup mandiri dan jauh dari orang tuanya”. 
